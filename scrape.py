STORAGE = BASE / "storage_state.json"
OUT_DIR = Path(os.environ.get("OUTPUT_DIR", BASE))
OUT_INTERNAL = OUT_DIR / "ranking_realtime_internal.json"
OUT_LIVERS = OUT_DIR / "ranking_realtime_livers.json"

