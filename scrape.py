"""
Backstage realtime ranking scraper.

Strategy:
1. Load saved session (storage_state.json)
2. Open revenue/task page (msToken/X-Bogus/X-Gnarly auto-generated by page JS)
3. Call get_anchor_settle_detail_v2 via page.evaluate fetch (in-page context)
   — this lets Backstage JSSDK auto-add security tokens
4. Paginate 100 × 8 for all 768 creators
5. Output JSON with rank / nickname / username / diamonds / last month / isLive / manager etc.
"""

